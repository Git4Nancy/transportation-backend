package com.example.demo.model;

public class RegistrationView {
	private Long id;
	private String companyName;
	private String driverName;
	private String driverPh;
	private String vehicleName;
	private String arvlDate;
	private String dptDate;
	private String guest;
	private String comments;
	private String status;
	private String invoiceId;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	public String getDriverPh() {
		return driverPh;
	}
	public void setDriverPh(String driverPh) {
		this.driverPh = driverPh;
	}
	public String getVehicleName() {
		return vehicleName;
	}
	public void setVehicleName(String vehicleName) {
		this.vehicleName = vehicleName;
	}
	public String getArvlDate() {
		return arvlDate;
	}
	public void setArvlDate(String arvlDate) {
		this.arvlDate = arvlDate;
	}
	public String getDptDate() {
		return dptDate;
	}
	public void setDptDate(String dptDate) {
		this.dptDate = dptDate;
	}
	public String getGuest() {
		return guest;
	}
	public void setGuest(String guest) {
		this.guest = guest;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getInvoiceId() {
		return invoiceId;
	}
	public void setInvoiceId(String invoiceId) {
		this.invoiceId = invoiceId;
	}
	
	


}
